<h2>The Power Of Open Source</h2>
<p>The Clickbooth family is committed to the public collaboration and increased social good that comes from giving back to the Open Source community. With collaboration and public sharing of knowledge we can all collectively move forward much faster as a society. Clickbooth enthusiastically contributes to and supports many Open Source technologies.</p>

<h2>Tracking202 (T202)</h2>
<p><a href="http://www.tracking202.com/">Tracking202 Inc</a>, a PPC Affiliate Marketing Analytics company based in San Francisco, CA, developed the powerful framework that powers CB's XTracks. T202 was founded by Wes Mahler, a Silicon Valley Internet Entrepreneur and Real Estate Investor. We are thankful to our friends at T202 for releasing the original code to the Open Source community and we are excited to further contribute to the collaborative effort that will increase the success of affiliate marketers worldwide.</p>

<h2>Why XTracks?</h2>
<p>XTracks began as a pilot-extension of Tracking202 by Clickbooth developers who realized that many online marketers have the pressing need of a more extensive feature set. Much like Firefox began as an "experiment" out of the Mozilla project, it was soon apparent that XTracks would become an entirely new and interesting project with a lot of long-term potential.</p>
<p>Though we could have developed our own in-house version from scratch, we decided this would be a great opportunity to contribute to the Open Source community.</p>

<h2>What is the relationship between Tracking202 and XTracks?</h2>
<p>Tracking202 and XTracks share the common goal of empowering the online marketing community with powerful Open Source tools, albeit with no formal relationship between the two companies. When Tracking202 originally released the source code to the Open Source community, they made an empowering statement to the community. In order to show gratitude, we are looking to continue extending out that original vision and returning that technology to the open source and affiliate community FREE for all to use.</p>

<h2>How much does XTracks cost?</h2>
<p>XTracks is provided FREE to anyone; whether you work with us or not! We even offer our own hosted version, where you can simply login and begin tracking valuable marketing data, completely FREE!</p>

